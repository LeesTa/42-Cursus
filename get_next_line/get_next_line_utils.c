#include "get_next_line.h"

size_t	ft_strlen(const char *s)
{
	int			i;
	size_t		count;

	count = 0;
	i = 0;
	if (s == NULL)
		return (i);
	while (s[i])
	{
		count++;
		i++;
	}
	return (count);
}

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	if (!s)
		return (NULL);
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return ((char *)s + i);
		i++;
	}
	if (c != '\0')
		return (0);
	return ((char *)s + i);
}

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*ptr;

	ptr = ((char *)malloc(sizeof(char) * (ft_strlen(s1) + 1)));
	if (!ptr)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	i;
	size_t	j;

	str = (char *)malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if ((!s2) || (!s1) || !(str))
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	while (s2[j] != '\0')
	{
		str[i + j] = s2[j];
		j++;
	}
	str[i + j] = '\0';
	free((char *)s1);
	return (str);
}
