/*
 * returns 1 if is alphabetical (lowercase and uppercase too)
 * returns 0 if not
*/

int	ft_isalpha(int c)
{
	if (((c < 65) || (c > 122) || ((c > 90) && (c < 97))))
		return (0);
	return (1);
}
